#!/usr/bin/env node
/**
 * Read-only full-table export: classify each pmci.provider_markets row into
 * vocabulary templates and write CSV + markdown summary.
 *
 * Uses DATABASE_URL (batched pg queries of 500 rows). Does not modify the database.
 *
 * Outputs:
 *   state/template-classification/provider_market_templates.csv
 *   state/template-classification/classification-summary.md
 *
 * To persist the same classifier into the DB (optional), run:
 *   npm run pmci:backfill:vocabulary-templates
 * (see script header in pmci-backfill-vocabulary-templates.mjs). Rule-only backfill:
 *   npm run pmci:classify:templates
 */
import fs from "fs";
import path from "path";
import pg from "pg";
import { fileURLToPath } from "url";
import { loadEnv } from "../../src/platform/env.mjs";
import { classifyVocabularyTemplate, nullPatternKey } from "../../lib/classification/vocabulary-market-template.mjs";

loadEnv();
const { Client } = pg;

const BATCH = 500;
const OUT_DIR = path.join(
  fileURLToPath(new URL("../../state/template-classification", import.meta.url)),
);
const CSV_PATH = path.join(OUT_DIR, "provider_market_templates.csv");
const MD_PATH = path.join(OUT_DIR, "classification-summary.md");

function csvEscape(s) {
  if (s === null || s === undefined) return "";
  const str = String(s);
  if (/[",\n\r]/.test(str)) return `"${str.replace(/"/g, '""')}"`;
  return str;
}

function ensureDir(dir) {
  fs.mkdirSync(dir, { recursive: true });
}

async function main() {
  const databaseUrl = process.env.DATABASE_URL?.trim();
  if (!databaseUrl) {
    console.error("export-vocabulary-classification: DATABASE_URL is required");
    process.exit(1);
  }

  ensureDir(OUT_DIR);

  const client = new Client({ connectionString: databaseUrl });
  await client.connect();

  const countsByCatTemplate = new Map();
  const categoryTotals = new Map();
  const templateGlobal = new Map();
  const nullByPattern = new Map();

  let total = 0;
  let classified = 0;
  let lastId = 0;

  const header = ["id", "category", "market_template", "template_params", "classification_confidence"].join(",");
  fs.writeFileSync(CSV_PATH, header + "\n", "utf8");

  try {
    while (true) {
      const { rows } = await client.query(
        `SELECT id, provider_id, provider_market_ref, event_ref, title, category, status, sport, event_type, game_date, close_time
         FROM pmci.provider_markets
         WHERE id > $1
         ORDER BY id ASC
         LIMIT $2`,
        [lastId, BATCH],
      );

      if (!rows.length) break;

      const lines = [];
      for (const row of rows) {
        total += 1;
        lastId = Number(row.id);

        const cat = row.category || "";
        const out = classifyVocabularyTemplate(row);
        const tpl = out.market_template;
        const paramsJson = JSON.stringify(out.template_params ?? {});
        const conf = out.classification_confidence;

        if (tpl) classified += 1;

        categoryTotals.set(cat, (categoryTotals.get(cat) || 0) + 1);

        const tgKey = tpl ?? "∅";
        templateGlobal.set(tgKey, (templateGlobal.get(tgKey) || 0) + 1);

        const keyCat = cat || "(null)";
        const kt = `${keyCat}||${tpl ?? "∅"}`;
        countsByCatTemplate.set(kt, (countsByCatTemplate.get(kt) || 0) + 1);

        if (!tpl) {
          const pk = nullPatternKey(row.title);
          if (!nullByPattern.has(pk)) {
            nullByPattern.set(pk, { count: 0, sampleTitle: String(row.title || "").slice(0, 120), ids: [] });
          }
          const entry = nullByPattern.get(pk);
          entry.count += 1;
          if (entry.ids.length < 5) entry.ids.push(row.id);
        }

        lines.push(
          [
            csvEscape(row.id),
            csvEscape(cat),
            csvEscape(tpl ?? ""),
            csvEscape(paramsJson),
            csvEscape(conf != null ? String(conf) : ""),
          ].join(","),
        );
      }

      fs.appendFileSync(CSV_PATH, lines.join("\n") + "\n", "utf8");
      process.stderr.write(`\rprocessed id <= ${lastId}  total=${total}  `);
    }
  } finally {
    await client.end();
  }

  const rate = total ? (classified / total) * 100 : 0;

  // --- markdown ---
  const byCategory = new Map();
  for (const [kt, c] of countsByCatTemplate) {
    const [cat, tpl] = kt.split("||");
    if (!byCategory.has(cat)) byCategory.set(cat, []);
    byCategory.get(cat).push({ template: tpl === "∅" ? null : tpl, count: c });
  }

  let md = `# Provider market template classification\n\n`;
  md += `Generated by \`scripts/classify/export-vocabulary-classification.mjs\` (read-only; full table scan in batches of ${BATCH}).\n\n`;
  md += `**Note:** Supabase MCP was not available in this Cursor session; classification uses the same Postgres connection string as other PMCI scripts (\`DATABASE_URL\`).\n\n`;
  md += `## Totals\n\n`;
  md += `- **Rows processed:** ${total}\n`;
  md += `- **Classified (non-null template):** ${classified}\n`;
  md += `- **Unclassified (null template):** ${total - classified}\n`;
  md += `- **Classification rate:** ${(classified / (total || 1)).toFixed(6)} (${rate.toFixed(2)}%)\n`;
  md += `- **Distinct null title patterns:** ${nullByPattern.size}\n\n`;

  md += `## Global template counts (all categories)\n\n`;
  md += `| Template | Count | % of all rows |\n`;
  md += `|----------|------:|--------------:|\n`;
  const globalRows = [...templateGlobal.entries()].sort((a, b) => b[1] - a[1]);
  for (const [name, c] of globalRows) {
    const label = name === "∅" ? "∅ null" : name;
    const pct = ((c / total) * 100).toFixed(2);
    md += `| ${label} | ${c} | ${pct}% |\n`;
  }
  md += `\n`;

  md += `## Counts by category and template\n\n`;
  const sortedCats = [...byCategory.keys()].sort();
  for (const cat of sortedCats) {
    const rows = byCategory.get(cat).sort((a, b) => b.count - a.count);
    const catTotal = rows.reduce((s, r) => s + r.count, 0);
    md += `### ${cat}\n\n`;
    md += `Category subtotal: **${catTotal}**\n\n`;
    md += `| Template | Count | % of category |\n`;
    md += `|----------|------:|--------------:|\n`;
    for (const r of rows) {
      const label = r.template == null ? "∅ null" : r.template;
      const pct = catTotal ? ((r.count / catTotal) * 100).toFixed(2) : "0.00";
      md += `| ${label} | ${r.count} | ${pct}% |\n`;
    }
    md += `\n`;
  }

  md += `## Unclassified rows (null template), grouped by pattern\n\n`;
  const nullPatterns = [...nullByPattern.entries()]
    .filter(([, v]) => v.count >= 1)
    .sort((a, b) => b[1].count - a[1].count);

  for (const [pk, info] of nullPatterns.slice(0, 200)) {
    md += `### ${info.count}× — \`${pk.slice(0, 200)}\`\n`;
    md += `- Sample title: ${info.sampleTitle.replace(/\|/g, "\\|")}\n`;
    md += `- Example ids: ${info.ids.join(", ")}\n\n`;
  }
  if (nullPatterns.length > 200) {
    md += `\n_(${nullPatterns.length - 200} additional pattern groups omitted; see full distribution in analysis.)_\n\n`;
  }

  md += `## Suggested new templates (null clusters with >10 markets)\n\n`;
  const bigNull = nullPatterns.filter(([, v]) => v.count > 10);
  if (!bigNull.length) {
    md += `_None — no null cluster exceeded 10 markets._\n\n`;
  } else {
    for (const [pk, info] of bigNull) {
      const slug = pk
        .replace(/[^a-z0-9]+/g, "-")
        .replace(/^-|-$/g, "")
        .slice(0, 48);
      md += `- **\`${slug || "pattern-cluster"}\`** (${info.count} markets): candidate template key \`suggested-${slug || "cluster"}\` — refine from sample: "${info.sampleTitle.slice(0, 80)}..."\n`;
    }
    md += `\n`;
  }

  fs.writeFileSync(MD_PATH, md, "utf8");
  process.stderr.write(`\nWrote ${CSV_PATH}\nWrote ${MD_PATH}\n`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
