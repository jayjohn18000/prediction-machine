-- Execution intelligence: continuous executable edge events.
-- Source: prediction_market_spreads (same filter as executable_edges_feed).
-- An edge event = consecutive executable rows for same (candidate, event_id)
-- with observed_at within 120 seconds; gap > 120s starts a new event.
-- This migration performs a historical backfill; edge_events is not kept
-- automatically in sync with new prediction_market_spreads rows today.
-- Do not modify prediction_market_spreads or executable_edges_feed.

CREATE TABLE IF NOT EXISTS public.edge_events (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  candidate text NOT NULL,
  event_id text NOT NULL,
  edge_start timestamptz NOT NULL,
  edge_end timestamptz NOT NULL,
  duration_seconds integer NOT NULL,
  max_edge double precision NOT NULL,
  avg_edge double precision NOT NULL,
  observations integer NOT NULL
);

COMMENT ON TABLE public.edge_events IS 'Continuous executable edge events: sequences of executable_edges_feed rows within 120s for same candidate+event_id.';
COMMENT ON COLUMN public.edge_events.edge_start IS 'First observed_at in the sequence.';
COMMENT ON COLUMN public.edge_events.edge_end IS 'Last observed_at in the sequence.';
COMMENT ON COLUMN public.edge_events.duration_seconds IS 'edge_end - edge_start in seconds.';
COMMENT ON COLUMN public.edge_events.max_edge IS 'Max (kalshi_yes_bid - polymarket_yes_ask) during the sequence.';
COMMENT ON COLUMN public.edge_events.avg_edge IS 'Average executable edge during the sequence.';
COMMENT ON COLUMN public.edge_events.observations IS 'Count of spread observations in the sequence.';

CREATE INDEX IF NOT EXISTS idx_edge_events_candidate_event_id
  ON public.edge_events (candidate, event_id);
CREATE INDEX IF NOT EXISTS idx_edge_events_edge_start
  ON public.edge_events (edge_start DESC);

CREATE UNIQUE INDEX IF NOT EXISTS idx_edge_events_candidate_event_id_edge_start
  ON public.edge_events (candidate, event_id, edge_start);

-- Populate from prediction_market_spreads (source of truth) using same
-- executable condition as executable_edges_feed. Window logic: partition by
-- (candidate, event_id), order by observed_at; new group when gap to previous
-- row > 120 seconds (or first row in partition).
INSERT INTO public.edge_events (
  candidate,
  event_id,
  edge_start,
  edge_end,
  duration_seconds,
  max_edge,
  avg_edge,
  observations
)
WITH executable AS (
  SELECT
    candidate,
    event_id,
    observed_at,
    (kalshi_yes_bid - polymarket_yes_ask) AS executable_edge
  FROM public.prediction_market_spreads
  WHERE kalshi_yes_bid IS NOT NULL
    AND polymarket_yes_ask IS NOT NULL
    AND kalshi_yes_bid > polymarket_yes_ask
),
with_gap AS (
  SELECT
    candidate,
    event_id,
    observed_at,
    executable_edge,
    observed_at - LAG(observed_at) OVER (
      PARTITION BY candidate, event_id
      ORDER BY observed_at
    ) AS gap
  FROM executable
),
with_group AS (
  SELECT
    candidate,
    event_id,
    observed_at,
    executable_edge,
    SUM(CASE
      WHEN gap IS NULL OR gap > interval '120 seconds' THEN 1
      ELSE 0
    END) OVER (
      PARTITION BY candidate, event_id
      ORDER BY observed_at
    ) AS grp
  FROM with_gap
)
SELECT
  candidate,
  event_id,
  MIN(observed_at) AS edge_start,
  MAX(observed_at) AS edge_end,
  EXTRACT(EPOCH FROM (MAX(observed_at) - MIN(observed_at)))::integer AS duration_seconds,
  MAX(executable_edge)::double precision AS max_edge,
  AVG(executable_edge)::double precision AS avg_edge,
  COUNT(*)::integer AS observations
FROM with_group
GROUP BY candidate, event_id, grp
ON CONFLICT (candidate, event_id, edge_start) DO NOTHING;

GRANT SELECT ON public.edge_events TO anon;
GRANT SELECT ON public.edge_events TO authenticated;
