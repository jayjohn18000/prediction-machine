-- PMCI Phase 1 schema (fragmentation-first, provider-agnostic)
-- Drop-in for Supabase Postgres
-- Notes:
-- - Uses schema `pmci` to avoid colliding with existing tables.
-- - Includes a "current links" view to pick latest link_version per (family, provider_market).
-- - Keeps link evidence in `market_links.reasons` jsonb (MVP explainability).

create schema if not exists pmci;

-- Supabase usually has pgcrypto enabled, but don't gamble.
create extension if not exists pgcrypto;

-- Updated-at helper
create or replace function pmci.set_updated_at()
returns trigger language plpgsql as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

-- Types
do $$ begin
  create type pmci.market_type as enum ('binary','range','scalar');
exception when duplicate_object then null; end $$;

do $$ begin
  create type pmci.relationship_type as enum ('identical','equivalent','proxy','correlated');
exception when duplicate_object then null; end $$;

do $$ begin
  create type pmci.unmatched_reason as enum (
    'no_title_match',
    'no_time_overlap',
    'resolution_source_mismatch',
    'structure_mismatch',
    'low_confidence',
    'awaiting_manual_review'
  );
exception when duplicate_object then null; end $$;

-- Providers
create table if not exists pmci.providers (
  id         smallint generated by default as identity primary key,
  code       text not null unique, -- 'kalshi','polymarket'
  name       text not null,
  created_at timestamptz not null default now()
);

insert into pmci.providers (code, name)
values ('kalshi','Kalshi'), ('polymarket','Polymarket')
on conflict (code) do nothing;

-- Canonical event/market/outcome
create table if not exists pmci.canonical_events (
  id                uuid primary key default gen_random_uuid(),
  slug              text not null unique,
  title             text not null,
  category          text not null,
  description       text,
  start_time        timestamptz,
  end_time          timestamptz,
  resolution_source text,
  metadata          jsonb not null default '{}'::jsonb,
  created_at        timestamptz not null default now(),
  updated_at        timestamptz not null default now()
);
create index if not exists idx_pmci_canonical_events_category
  on pmci.canonical_events(category);

drop trigger if exists trg_pmci_canonical_events_updated_at on pmci.canonical_events;
create trigger trg_pmci_canonical_events_updated_at
before update on pmci.canonical_events
for each row execute function pmci.set_updated_at();

create table if not exists pmci.canonical_markets (
  id                 uuid primary key default gen_random_uuid(),
  canonical_event_id  uuid not null references pmci.canonical_events(id) on delete cascade,
  label              text not null,
  market_type        pmci.market_type not null,
  resolution_rules   text,
  close_time         timestamptz,
  metadata           jsonb not null default '{}'::jsonb,
  created_at         timestamptz not null default now(),
  updated_at         timestamptz not null default now()
);
create index if not exists idx_pmci_canonical_markets_event
  on pmci.canonical_markets(canonical_event_id);

drop trigger if exists trg_pmci_canonical_markets_updated_at on pmci.canonical_markets;
create trigger trg_pmci_canonical_markets_updated_at
before update on pmci.canonical_markets
for each row execute function pmci.set_updated_at();

create table if not exists pmci.canonical_outcomes (
  id                 uuid primary key default gen_random_uuid(),
  canonical_market_id uuid not null references pmci.canonical_markets(id) on delete cascade,
  name               text not null,
  sort_order         int not null default 0,
  metadata           jsonb not null default '{}'::jsonb
);
create index if not exists idx_pmci_canonical_outcomes_market
  on pmci.canonical_outcomes(canonical_market_id);

-- Provider markets + snapshots
create table if not exists pmci.provider_markets (
  id                 bigint generated by default as identity primary key,
  provider_id        smallint not null references pmci.providers(id),
  provider_market_ref text not null, -- provider's native id/ticker (text)
  event_ref          text,
  title              text not null,
  category           text,
  url                text,
  market_type        pmci.market_type,
  resolution_source  text,
  open_time          timestamptz,
  close_time         timestamptz,
  status             text,
  metadata           jsonb not null default '{}'::jsonb,
  first_seen_at      timestamptz not null default now(),
  last_seen_at       timestamptz not null default now()
);
create unique index if not exists ux_pmci_provider_markets_identity
  on pmci.provider_markets(provider_id, provider_market_ref);
create index if not exists idx_pmci_provider_markets_status
  on pmci.provider_markets(status);
create index if not exists idx_pmci_provider_markets_category
  on pmci.provider_markets(category);

create table if not exists pmci.provider_market_snapshots (
  id                 bigint generated by default as identity primary key,
  provider_market_id bigint not null references pmci.provider_markets(id) on delete cascade,
  observed_at        timestamptz not null,
  price_yes          numeric(10,6),
  price_no           numeric(10,6),
  best_bid_yes       numeric(10,6),
  best_ask_yes       numeric(10,6),
  liquidity          numeric(20,4),
  volume_24h         numeric(20,4),
  raw                jsonb not null default '{}'::jsonb
);
create index if not exists idx_pmci_snapshots_market_time
  on pmci.provider_market_snapshots(provider_market_id, observed_at desc);

-- Market families
create table if not exists pmci.market_families (
  id                  bigint generated by default as identity primary key,
  canonical_event_id   uuid references pmci.canonical_events(id) on delete set null,
  canonical_market_id  uuid references pmci.canonical_markets(id) on delete set null,
  label               text not null,
  notes               text,
  created_at          timestamptz not null default now(),
  updated_at          timestamptz not null default now()
);
create index if not exists idx_pmci_market_families_event
  on pmci.market_families(canonical_event_id);

drop trigger if exists trg_pmci_market_families_updated_at on pmci.market_families;
create trigger trg_pmci_market_families_updated_at
before update on pmci.market_families
for each row execute function pmci.set_updated_at();

-- Links
create table if not exists pmci.market_links (
  id                   bigint generated by default as identity primary key,
  family_id            bigint not null references pmci.market_families(id) on delete cascade,
  provider_id          smallint not null references pmci.providers(id),
  provider_market_id   bigint not null references pmci.provider_markets(id) on delete cascade,
  relationship_type    pmci.relationship_type not null,
  status               text not null default 'active',
  link_version         int not null,
  confidence           numeric(5,4) not null,

  correlation_window   interval,
  lag_seconds          int,
  correlation_strength numeric(5,4),
  break_rate           numeric(5,4),
  last_validated_at    timestamptz,
  staleness_score      numeric(5,4),

  reasons              jsonb not null default '{}'::jsonb,

  created_at           timestamptz not null default now(),
  updated_at           timestamptz not null default now()
);

create unique index if not exists ux_pmci_market_links_identity
  on pmci.market_links(family_id, provider_market_id, link_version);

create index if not exists idx_pmci_market_links_family on pmci.market_links(family_id);
create index if not exists idx_pmci_market_links_provider_market on pmci.market_links(provider_id, provider_market_id);
create index if not exists idx_pmci_market_links_relationship on pmci.market_links(relationship_type);
create index if not exists idx_pmci_market_links_status on pmci.market_links(status);

drop trigger if exists trg_pmci_market_links_updated_at on pmci.market_links;
create trigger trg_pmci_market_links_updated_at
before update on pmci.market_links
for each row execute function pmci.set_updated_at();

create or replace view pmci.v_market_links_current as
select distinct on (family_id, provider_market_id)
  *
from pmci.market_links
where status <> 'removed'
order by family_id, provider_market_id, link_version desc, updated_at desc;

-- Unmatched backlog
create table if not exists pmci.unmatched_markets (
  id                 bigint generated by default as identity primary key,
  provider_id        smallint not null references pmci.providers(id),
  provider_market_id bigint not null references pmci.provider_markets(id) on delete cascade,
  reason             pmci.unmatched_reason not null,
  details            text,
  active             boolean not null default true,
  first_seen_at      timestamptz not null default now(),
  last_seen_at       timestamptz not null default now()
);
create index if not exists idx_pmci_unmatched_provider_reason
  on pmci.unmatched_markets(provider_id, reason);

-- Linker eval harness
create table if not exists pmci.linker_runs (
  id          bigint generated by default as identity primary key,
  version     int not null unique,
  description text,
  created_at  timestamptz not null default now()
);

create table if not exists pmci.link_gold_labels (
  id                    bigint generated by default as identity primary key,
  provider_a_id         smallint not null references pmci.providers(id),
  provider_a_market_id  bigint not null references pmci.provider_markets(id),
  provider_b_id         smallint not null references pmci.providers(id),
  provider_b_market_id  bigint not null references pmci.provider_markets(id),
  true_relationship     pmci.relationship_type not null,
  notes                 text,
  created_at            timestamptz not null default now()
);
create unique index if not exists ux_pmci_link_gold_pair
  on pmci.link_gold_labels(provider_a_id, provider_a_market_id, provider_b_id, provider_b_market_id);

create table if not exists pmci.linker_run_metrics (
  id                bigint generated by default as identity primary key,
  linker_run_id     bigint not null references pmci.linker_runs(id) on delete cascade,
  relationship_type pmci.relationship_type not null,
  tp                int not null,
  fp                int not null,
  fn                int not null,
  precision         numeric(5,4) not null,
  recall            numeric(5,4) not null,
  created_at        timestamptz not null default now()
);
create unique index if not exists ux_pmci_linker_run_rel_type
  on pmci.linker_run_metrics(linker_run_id, relationship_type);
