-- Time-series table for prediction market spread observations (data capture v1).
-- Append-only: observer inserts rows; no updates or deletes.
CREATE TABLE IF NOT EXISTS public.prediction_market_spreads (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  event_id text NOT NULL,
  candidate text NOT NULL,
  kalshi_yes double precision NOT NULL,
  polymarket_yes double precision NOT NULL,
  spread double precision NOT NULL,
  observed_at timestamptz NOT NULL DEFAULT now(),
  source_meta jsonb
);

COMMENT ON TABLE public.prediction_market_spreads IS 'Observation-only spread records: kalshi_yes - polymarket_yes per candidate.';

CREATE INDEX IF NOT EXISTS idx_prediction_market_spreads_candidate_observed_at
  ON public.prediction_market_spreads (candidate, observed_at DESC);

CREATE INDEX IF NOT EXISTS idx_prediction_market_spreads_event_id_observed_at
  ON public.prediction_market_spreads (event_id, observed_at DESC);
