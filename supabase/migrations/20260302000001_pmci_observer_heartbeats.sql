create table if not exists pmci.observer_heartbeats (
  id                      bigint generated by default as identity primary key,
  cycle_at                timestamptz not null default now(),
  pairs_attempted         int not null default 0,
  pairs_succeeded         int not null default 0,
  kalshi_fetch_errors     int not null default 0,
  polymarket_fetch_errors int not null default 0,
  spread_insert_errors    int not null default 0,
  pmci_ingestion_errors   int not null default 0,
  json_parse_errors       int not null default 0
);

create index if not exists idx_pmci_observer_heartbeats_cycle_at
  on pmci.observer_heartbeats(cycle_at desc);
