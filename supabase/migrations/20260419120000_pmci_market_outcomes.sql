-- A1 pivot: settled outcomes for linked sports families (current row + append-only history for provider revisions).

create table if not exists pmci.market_outcomes (
  id                   bigint generated by default as identity primary key,
  provider_market_id   bigint not null unique references pmci.provider_markets (id) on delete cascade,
  provider_id          smallint not null references pmci.providers (id),
  winning_outcome      text not null,
  winning_outcome_raw  jsonb,
  resolved_at          timestamptz,
  resolution_source_observed text not null,
  raw_settlement       jsonb not null default '{}'::jsonb,
  ingested_at          timestamptz not null default now(),
  updated_at           timestamptz not null default now()
);

create index if not exists idx_pmci_market_outcomes_provider
  on pmci.market_outcomes (provider_id);

create index if not exists idx_pmci_market_outcomes_resolved_at
  on pmci.market_outcomes (resolved_at desc nulls last);

drop trigger if exists trg_pmci_market_outcomes_updated_at on pmci.market_outcomes;
create trigger trg_pmci_market_outcomes_updated_at
before update on pmci.market_outcomes
for each row execute function pmci.set_updated_at();

comment on table pmci.market_outcomes is 'Latest known settlement per provider_market (Kalshi / Polymarket). Upserted by resolution ingestion; see market_outcome_history for revisions.';

-- Append-only audit log: every ingest refresh inserts a row; provider revisions remain queryable.
create table if not exists pmci.market_outcome_history (
  id                   bigint generated by default as identity primary key,
  provider_market_id   bigint not null references pmci.provider_markets (id) on delete cascade,
  provider_id          smallint not null references pmci.providers (id),
  winning_outcome      text not null,
  winning_outcome_raw  jsonb,
  resolved_at          timestamptz,
  resolution_source_observed text not null,
  raw_settlement       jsonb not null default '{}'::jsonb,
  recorded_at          timestamptz not null default now()
);

create index if not exists idx_pmci_market_outcome_history_market_time
  on pmci.market_outcome_history (provider_market_id, recorded_at desc);

create or replace function pmci.forbid_market_outcome_history_mutation()
returns trigger
language plpgsql
as $$
begin
  raise exception 'pmci.market_outcome_history is append-only';
end;
$$;

drop trigger if exists trg_pmci_market_outcome_history_no_mutate on pmci.market_outcome_history;
create trigger trg_pmci_market_outcome_history_no_mutate
before update or delete on pmci.market_outcome_history
for each row execute function pmci.forbid_market_outcome_history_mutation();

comment on table pmci.market_outcome_history is 'Append-only log of settlement observations (including provider revisions). INSERT only.';
