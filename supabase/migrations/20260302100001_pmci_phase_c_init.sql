alter table pmci.observer_heartbeats
  add column if not exists pairs_configured int not null default 0;

create table if not exists pmci.request_log (
  id           bigint generated by default as identity primary key,
  logged_at    timestamptz not null default now(),
  method       text not null,
  path         text not null,
  status       int not null,
  latency_ms   int,
  api_key_hint text  -- last 4 chars of key, or null if unauthenticated route
);

create index if not exists idx_pmci_request_log_logged_at
  on pmci.request_log(logged_at desc);

create index if not exists idx_pmci_request_log_path
  on pmci.request_log(path, logged_at desc);

